                <p>Katherine McNamara trouxe simpatia e encantou o público do Palco Thunder by Cinemark Club durante a CCXP22. 
                    Em uma conversa com Maria Bopp, a artista contou sobre a carreira, a paixão pelo Brasil e o carinho dos fãs das franquias que já atuou.
                </p>
                <p>A atriz possui um enorme carinho pelo país, ela esteve presente na CCXP14 e foi sua primeira participação em uma convenção. "Adoro o Brasil e os brasileiros, 
                    o público é tão carinhoso, há tanto amor pela comunidade, é um prazer estar de volta" disse, encantada.
                </p>
                <p>McNamara é reconhecida pelos fãs por participar de produções de ficção e fantasia como Shadowhunters, Maze Runner e Arrow, e conta que, apesar de ter caído de 
                    paraquedas no gênero, sentiu uma conexão instantânea. "Sou grata, cresci com esses mundos [de fantasia] e queria vivenciar isso. Sei como é a responsabilidade 
                    de lidar com esses personagens e me sinto agradecida de poder vivenciá-los."
                </p>
                <p>Shadowhunters é uma das franquias pelas quais ela tem grande apreço, tanto criou ao lado do ator Dominic Sherwood o podcast Return to the Shadows, focado no 
                    universo e bastidores da série. Segundo a atriz, a programa aproximou-a dos fãs, e ela “jogou para o universo” o desejo em reviver a série - caso alguma produtora queira.
                </p>
                <p>Durante o painel, McNamara disse achar incrível como os eventos como CCXP são importantes para os fãs de franquias e atores e artistas. "Eu aprendi como a comunidade de 
                    convenções é incrível, a gente conta essas histórias porque a gente se empodera e traz um sentimento de pertencimento"
                </p>

